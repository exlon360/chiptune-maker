enum ChipWaveform: String, Codable, CaseIterable, Hashable, Identifiable {
    case pulse12
    case pulse25
    case pulse50
    case pulse75
    case triangle
    case saw
    case sine
    case pluck
    case noise
    case kick
    case snare
    case hat
    case tom

    var id: String { rawValue }

    var title: String {
        switch self {
        case .pulse12:
            return "12.5%"
        case .pulse25:
            return "25%"
        case .pulse50:
            return "50%"
        case .pulse75:
            return "75%"
        case .triangle:
            return "Tri"
        case .saw:
            return "Saw"
        case .sine:
            return "Sine"
        case .pluck:
            return "Pluck"
        case .noise:
            return "Noise"
        case .kick:
            return "Kick"
        case .snare:
            return "Snare"
        case .hat:
            return "Hat"
        case .tom:
            return "Tom"
        }
    }

    var dutyCycle: Double {
        switch self {
        case .pulse12:
            return 0.125
        case .pulse25:
            return 0.25
        case .pulse50:
            return 0.5
        case .pulse75:
            return 0.75
        case .triangle, .saw, .sine, .pluck, .noise, .kick, .snare, .hat, .tom:
            return 0.5
        }
    }

    var isPercussion: Bool {
        switch self {
        case .kick, .snare, .hat, .tom:
            return true
        case .pulse12, .pulse25, .pulse50, .pulse75, .triangle, .saw, .sine, .pluck, .noise:
            return false
        }
    }
}

