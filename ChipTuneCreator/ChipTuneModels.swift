        func row(_ name: String) -> Int {
            guard let note = MusicNote(trackerName: name),
                  let index = rows.firstIndex(of: note) else {
                return 0
            }
            return index
        }

        func note(_ name: String, step: Int, length: Int = 1, velocity: Double = 1.0) -> SequencerNote {
            SequencerNote(row: row(name), startStep: step, length: length, velocity: velocity)
        }

        struct StepToken {
            let name: String?
            let velocity: Double?
        }

        func sequence(_ steps: [StepToken], velocity: Double, sustainRepeats: Bool = true) -> [SequencerNote] {
            var events = [SequencerNote]()
            var previousName: String?

            for (step, token) in steps.enumerated() {
                guard let name = token.name else {
                    previousName = nil
                    continue
                }

                let tokenVelocity = token.velocity ?? velocity
                if sustainRepeats,
                   previousName == name,
                   let lastIndex = events.indices.last,
                   events[lastIndex].startStep + events[lastIndex].length == step,
                   abs(events[lastIndex].velocity - tokenVelocity) < 0.035 {
                    events[lastIndex].length += 1
                } else {
                    events.append(note(name, step: step, velocity: tokenVelocity))
                }

                previousName = name
            }

            return events
